package sample;

/** Provides definition of a "card" as a physical object */
public class Card {
    private int shape; //Krztalt
    private int colour; //Kolor

    public int getShape() {
        return shape;
    }

    public void setShape(int shape) {
        this.shape = shape;
    }

    public int getColour() {
        return colour;
    }

    public void setColour(int colour) {
        this.colour = colour;
    }
}
